<section id="info" aria-labelledby="about-4" class="about-block about-block--info">
    <h3 id="about-4">Information on the vote</h3>
    <img src="/images/choice.svg" class="about-block__info-thumbnail" alt="" aria-hidden="true" />
    <div class="columns">
        <h4>Who can take part in Altea Participa?</h4>
        <p>
            Any person and/or association may participate in this process by submitting
            their proposals.
        </p>

        <h4>Who can vote?</h4>
        <p>
            All citizens who are <strong>16 years of age or older and are
            residents of Altea</strong>, as per the census on <strong>August 30th, 2018</strong>.
        </p>

        <h4>What will be voted on?</h4>
        <p>
            You may choose up to <strong>3 out of the 10 proposals</strong> previously selected
            by the <strong>Citizen Participation Council</strong> from among all the submissions.
            If more than three projects are crossed on a ballot, it will be considered a spoilt ballot.
        </p>

        <h4>Will I be able to vote on-line?</h4>
        <p>
            You may cast your vote on-line by accessing this website on the polling days
            from any computer or mobile device. For security reasons, only 3 votes are allowed
            per IP. You will also have to enter your DNI, NIE or Passport where indicated.
            Any person who is unable to cast a vote because their ID has already been used
            or because they want to rectify their vote may go to any of the authorized polling
            stations and ask a staff member to annul and reacast the ballot after showing
            a valid form of ID.
        </p>

        <h4>Start and end of the vote</h4>
        <p>
            On-line voting will open on December 3rd 2018 at midnight and will
            close on December 16th at midnight. In-person voting will also be available
            at selected <a href="#voting-places" data-scroll>polling stations</a>
            during the same days on their normal opening hours.
        </p>

        <h4>Results</h4>
        <p>
            The results of the vote will be made public on this website
            as soon as the voting period ends.
        </p>
    </div>
</section>

