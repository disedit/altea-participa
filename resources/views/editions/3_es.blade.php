<section aria-labelledby="about-1" class="about-block" aria-live="polite">
  <h3 id="about-1">Altea imagina, Altea participa</h3>
  <img src="/images/choice-2.svg" class="about-block__thumbnail" alt="" aria-hidden="true" />

  <p class="about-block__subtitle">
      La ciudadanía decide directamente parte de las inversiones municipales con un presupuesto de 100.000€
  </p>

  <p>
      El <strong>Ayuntamiento de Altea</strong> ha establecido la regeneración democrática como uno de sus pilares básicos. Una regeneración que pasa por dar voz a los ciudadanos y ciudadanas y acercarlos a la toma de decisiones. Una participación que tiene por finalidad que la ciudadanía pueda conseguir mayores cuotas de corresponsabilidad política para que <strong>“co-gobiernen” su ayuntamiento, su pueblo</strong>.
  </p>

  <p>
      Ya empezamos <a href="{{ url('/archive/2017') }}">hace tres años</a> con este formato de presupuestos participativos y en esta segunda edición, el proceso de participación será mucho más amplio, dinámico y fácil.
  </p>

  <p>
      <strong>Altea Participa</strong> pretende profundizar en la participación real y efectiva de la ciudadanía.
  </p>

  <div id="about-1-more" class="about-block__01-hidden">
      <p>
          A tal efecto, el Equipo de Gobierno ha decidido que los 10 proyectos que se sometan a votación hayan sido propuestos por la ciudadanía. En el mes de septiembre de 2019, se abrirá un plazo de tres semanas para que los ciudadanos/as presenten sus propuestas de inversiones. Estas propuestas serán evaluadas y analizadas por los Técnicos Municipales donde cada propuesta tendrá un presupuesto estimado y a quien beneficiaría, así como el motivo por el que quedarán desestimadas.
      </p>

      <p>
          Las propuestas que superen la estimación técnica, serán debatida en el seno del Consejo de Participación Ciudadana, el órgano máximo de participación, donde se decidirán los 10 proyectos que se someterán a votación. Hay que recordar que en el Consejo de Participación Ciudadana están representados todos los grupos políticos y de los Consejos Sectoriales creados (Consejo Escolar Municipal, de Medio Ambiente, de Agricultura, de Salud, de Urbanismo, de Comercio y Turismo, de los Niños y Niñas).
      </p>

      <p>
          Posteriormente serán los ciudadanos y ciudadanas quienes se pronuncien sobre qué 3 proyectos consideran prioritarios. La información sobre los proyectos se podrá consultar en la web municipal, en los <a href="#voting-places" data-scroll>sitios habilitados</a> para la emisión de la opinión de la ciudadanía, así como en los distintos medios de comunicación. Se hará una presentación pública de los proyectos sometidos a votación..
      </p>

      <p>
          Las personas y/o asociaciones que hayan propuesto los proyectos elegidos para ser votados tendrán la oportunidad de presentarlos públicamente en sesión abierta a la ciudadanía, sin perjuicio de que la persona o grupo que así lo considere pueda realizar más acciones de promoción de su propuesta.
      </p>
  </div>

