<section id="info" aria-labelledby="about-4" class="about-block about-block--info">
  <h3 id="about-4">Información sobre la votación</h3>
  <img src="/images/choice.svg" class="about-block__info-thumbnail" alt="" aria-hidden="true" />
  <div class="columns">
      <h4>¿Quién puede participar en Altea Participa?</h4>
      <p>
        Podrán participar en este proceso todas aquellas personas y/o asociaciones que así lo deseen.
      </p>

      <h4>¿Quién puede votar?</h4>
      <p>
          Podrán votar en este proceso aquellas personas mayores de 16 años empadronadas en Altea.
      </p>

      <h4>¿Qué se votará?</h4>
      <p>
        Se podrán votar 3 de las 10 propuestas elegidas previamente por el Consejo de Participación Ciudadana de entre las presentadas por la ciudadanía. La decisión que manifieste un ciudadano o ciudadana será ineficaz si se eligen más de 3 proyectos.
      </p>

      <h4>¿Se podrá votar por internet?</h4>
      <p>
        Se podrá votar mediante una plataforma de internet y con un sistema de votación telemática accesible desde cualquier ordenador o dispositivo móvil. Por cada línea IP se podrán efectuar 3 votaciones. En esta web, se tendrá que introducir previamente el DNI en la casilla que se indique. Aquellas personas que descubran que alguien ha suplantado su identidad o quieran rectificar su voto, podrán dirigirse a cualquiera de los puntos de votación ubicados en los edificios municipales y pedir a la persona encargada la anulación del voto emitido y la voluntad de volver a votar, presentando el DNI.
      </p>

      <h4>Inicio y fin de la votación</h4>
      <p>
        Se ha establecido un periodo de votación que empezará, vía internet, a las 12 horas de la noche del día 1 de febrero de 2021 y concluirá el día 14 a las 23:59.
      </p>

      <h4>Resultado de la votación</h4>
      <p>
          El resultado de la votación se hará público en esta web tan pronto concluya el periodo de votación.
      </p>
  </div>
</section>

