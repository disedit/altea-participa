<div class="about-block">
    <h3>Información sobre la votación</h3>
    <img src="/images/choice.svg" class="about-block__info-thumbnail" />
    <div class="columns">
        <h4>¿Quién puede participar en Altea Participa?</h4>
        <p>Podrán participar en este proceso aquellas personas <strong>mayores de 16 años empadronadas en Altea</strong>, de acuerdo con el padrón a fecha de <strong>01/06/2018</strong>.</p>

        <h4>¿Qué se votará?</h4>
        <p>Se podrán votar <strong>3 de las 10 propuestas</strong> elegidas previamente por el <strong>Consejo de Participación Ciudadana</strong> de entre las presentadas per los ciudadanos. La decisión que manifeste un ciudadano o ciudadana será ineficaz si se eligen más de 3 proyectos.</p>

        <h4>¿Se podrá votar por internet?</h4>
        <p>Se podrá votar mediante una plataforma de internet y con un sistema de <strong>votación telemática</strong> accesible desde cualquier ordenador o dispositivo móvil. Por cada línea IP se podrán efectuar 3 votaciones. En esta web se tendrá que introducir previamente el DNI en la casilla que se indique. Aquellas personas que descubran que alguien ha suplantado su identidad o quieran rectificar su voto, podrán dirigirse a cualquiera de los puntos de votación ubicados en los edificios municipales y pedir a la persona encargada la anulación del voto emitido y volver a votar, presentando su DNI.</p>

        <h4>Inicio y final de la votación</h4>
